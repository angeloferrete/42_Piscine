#include <unistd.h>

void	ft_print_comb2(void)
{
	int		n1;
	int		n2;
	char	pos[5];

	n1 = 0;
	pos[2] = ' ';
	while (n1 < 99)
	{
		pos[0] = n1 / 10 + '0';
		pos[1] = n1 % 10 + '0';
		n2 = n1 + 1;
		while (n2 < 100)
		{
			pos[3] = n2 / 10 + '0';
			pos[4] = n2 % 10 + '0';
			write(1, &pos, 5);
			if (n1 != 98)
				write(1, ", ", 2);
			n2++;
		}
	n1++;
	}
}
int	main(void)
{
	ft_print_comb2();
	return (0);
}
